def main():
    docs = []
    for intent in INTENTS.values():
        for q in intent["q"]:
            for a in intent["a"]:
                docs.append(f"<|u|>{q}\n<|b|>{a}\n<|endofdialog|>")
    # двухходовые диалоги: приветствие + вопрос об идентичности
    for greet_q, greet_a in zip(INTENTS["greeting"]["q"], INTENTS["greeting"]["a"]):
        for q in INTENTS["who"]["q"][:4]:
            a = rng.choice(INTENTS["who"]["a"])
            docs.append(f"<|u|>{greet_q}\n<|b|>{greet_a}\n<|endofdialog|>\n<|u|>{q}\n<|b|>{a}\n<|endofdialog|>")

    rng.shuffle(docs)
    path = os.path.join(OUT, "persona.txt")
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(docs) + "\n")
    print(f"persona.txt: {len(docs)} диалогов, ~{sum(len(d) for d in docs)/1e3:.0f}k символов")


if __name__ == "__main__":
    main()
